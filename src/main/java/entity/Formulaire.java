    private void JBEnregistrerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBEnregistrerActionPerformed
    int id = Integer.parseInt(JTFID.getText());
       String Nom = JTFNom.getText();
       String Prenom = JTFPrenom.getText();
       int age = (int) JSPINAge.getValue();
       String email = JTFemail.getText();
       String profession = JCProfession.getSelectedItem().toString();

       String civilite = null;
       if(JRBMme.isSelected()){
           civilite = "Mme"; 
       }
       if(JRBMlle.isSelected()){
           civilite = "Mlle";
       }
       if(JRBMonsieur.isSelected()){
           civilite = "Monsieur";
       }
       
       Participant p = new Participant(id, Nom, Prenom, age, email, profession, civilite);
       Participant.listeParticipant.add(p);
       
       DefaultTableModel tbl = (DefaultTableModel) JTABLE.getModel();
       tbl.addRow(new Object[]{
           p.ID,p.Nom,p.Prenom,p.age,p.Email,p.Profession,p.Civilite,});

       for(int i=0;i<Participant.listeParticipant.size();i++){
        System.out.println("linked list add: "+ Participant.listeParticipant.get(i));}
       
       
        
    }//GEN-LAST:event_JBEnregistrerActionPerformed

