    private void JBSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBSupprimerActionPerformed
        // TODO add your handling code here:             
        DefaultTableModel tbl = (DefaultTableModel) JTABLE.getModel();
             
        tbl.removeRow(JTABLE.getSelectedRow());
  
        JTFID.setText("");
        JTFNom.setText("");
        JTFPrenom.setText("");
        JSPINAge.setValue(0);
        JTFemail.setText("");
        JCProfession.setSelectedIndex(0);
        JRBMme.setSelected(false);
        JRBMlle.setSelected(false);
        JRBMonsieur.setSelected(false);
        
        Participant.listeParticipant.remove();
        for(int i=0;i<Participant.listeParticipant.size();i++){
        System.out.println("linked list remove: "+ Participant.listeParticipant.get(i));}
        JOptionPane.showMessageDialog(this, "Row removed successfully");
    }//GEN-LAST:event_JBSupprimerActionPerformed

    private void JTABLEMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTABLEMouseClicked
        int row = JTABLE.getSelectedRow(); 
        
        DefaultTableModel model = (DefaultTableModel)JTABLE.getModel();

        
        JTFID.setText(model.getValueAt(row,0).toString());
        JTFNom.setText(model.getValueAt(row,1).toString());
        JTFPrenom.setText(model.getValueAt(row,2).toString());
        JSPINAge.setValue((int)model.getValueAt(row,3));
        JTFemail.setText(model.getValueAt(row, 4).toString());
        String profession = JTABLE.getValueAt(row,5).toString();
        String civilite = JTABLE.getValueAt(row, 6).toString();
            
 
        switch(profession){
            case "Administrateur":
                JCProfession.setSelectedIndex(0);
        break;
            case "Professeur":
                JCProfession.setSelectedIndex(1);
        break;
            case "Etudiant":
                JCProfession.setSelectedIndex(2);
        break;
        }
        if(civilite.equals("Mme")){
                JRBMme.setSelected(true);
            }
            if(civilite.equals("Mlle")){
                JRBMlle.setSelected(true);
            }
            if(civilite.equals("Monsieur")){
                JRBMonsieur.setSelected(true);
            }
            
       
    }//GEN-LAST:event_JTABLEMouseClicked

